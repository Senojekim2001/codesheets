export function getAllSheetPaths() {
  const paths = []
  for (const domain of catalog) {
    for (const sheet of domain.sheets) {
      paths.push({ params: { domain: domain.id, sheet: sheet.id } })
    }
  }
  return paths
}

export function getSheet(domainId, sheetId) {
  const domain = catalog.find(d => d.id === domainId)
  if (!domain) return null
  const sheet = domain.sheets.find(s => s.id === sheetId)
  if (!sheet) return null
  return { domain, sheet }
}
